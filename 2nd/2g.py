# Python code to iterate through all values in a dictionary
statesAndCapitals = {
'Gujarat': 'Gandhinagar',
'Maharashtra': 'Mumbai',
'Rajasthan': 'Jaipur',
'Bihar': 'Patna'
}
print('List Of given capitals:\n')
# Iterating over values
for capital in statesAndCapitals.values():
    print(capital)
